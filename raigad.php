      <br><br>
    He was successor of the realm after his father's death, and ruled it for nine years. Chhatrapati Sambhaji maharaj’s  rule was largely shaped by the ongoing wars between the Maratha kingdom and Mughal Empire as well as other neighbouring powers such as the Siddis, Mysore and the Portuguese in Goa. As the Mughals Invaded Deccan which started a 27year war between Marathas and Mughals, Chhatrapati Sambhaji Maharaj Protecting Swarajya for almost 9 YEARS from Mughals but in 1689, Chhatrapati Sambhaji Maharaj was captured near Sangameshwar and then was took as a prisoner where he was brutally tortured and executed by the Mughals.
