    <p><b>
      Lingana is a massive pinnacle on the mainline of the Sahyadris between Raigad and Torna. It is in the Mahad Taluka of Raigad district. It is 35 km away from Mahad town. It takes its name from its shape which is like a linga. It is 2969 feet high with an ascent of four miles, the first half is easy to climb while the second is difficult. Its rock cut steps have been destroyed and the fort is almost completely inaccessible. The top of the fortified rock is 2500 feet square. No fortifications or buildings remain but there are traces of a grain store and some cisterns. Lingana fort was built by Chhatrapati Shivaji Maharaj in 1648 to secure central Konkan against the Sidis. Under the Marathas, Lingana was used as a penal settlement. The prisoners were confined to rock dungeons with one dungeon holding 50 prisoners. Lingana was captured by a Colonel Prother in 1818.
