      <p><b>
       After the death of Chhatrapati Sambhaji Maharaj, Rajaram Maharaj was crowned at Raigad on 12 March 1689. As the Mughals started laying siege to the region around Raigad on 25 March 1689, the widow of Chhatrapati Sambhaji Maharaj (Maharani Yesubai) and her minister Ramchandra Pant Amatya sent young Rajaram to the stronghold of Pratapgad through Kavlya ghat. 
      <br><br>
      The Maratha army fought with the Mughals and led the new Maratha king, Chhatrapati Rajaram Maharaj to escape through Kavlya ghat to the fort of Jinji in present-day state of Tamil Nadu via Pratapgad and Vishalgad forts, Chhatrapati Rajaram Maharaj reached Keladi (Near present-day Sagar in Karnataka) in disguise and sought refuge from Keladi Chennamma - The brave Kannada queen fought the Mughals and ensured safe passage and escape of Chhatrapati Rajaram Maharaj to Jinji where he reached after a month and a half on 1 November 1689, Keladi Chennamma fought the guerrilla warfare which frustrated the Mughals and the Mughals proposed peace accord for the first time with an Indian ruler, Keladi Chennamma. Details of this escape are known from the incomplete poetical biography of Rajaram, the Rajaramacharita written by his Rajpurohit, Keshav Pandit, in Sanskrit.
