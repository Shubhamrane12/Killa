                <p><b>
                  Raigad is a hill fort situated in Mahad, Raigad district of Maharashtra, India. Many constructions and structures that we see on raigad were built by chatrapati Shivaji when he made it his capital in 1674 when he was crowned the King of the Maratha Kingdom which later developed into the Maratha Empire, eventually covering much of western and central India. The fort rises 820 metres (2,700 ft) above the sea level and is located in the Sahyadri mountain range. There are approximately 1737 steps leading to the fort. The Raigad Ropeway, an aerial tramway exists.The height of this ropeway is 400 meters in height and 750 meters in length and takes only 4 minutes.
