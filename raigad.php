    <p><b>
        History of Raigad:
    </b></p>
    <p><b>
      In 1656 Chhatrapati Shivaji Maharaj Seized the fort, then known as the fort of Rairi from Chandraraoji More, the king of javali (jawali). <br><br>
      After capturing this fort Chhatrapati Shivaji Maharaj seeing the natural protection this fort had and also strategically important place in future as From Rairi one can keep an eye on Panvel and the Konkan region and seeing the adjoining mountains surrounding and protecting the fort from all sides, Maharaj said “तक्तास जागा हाच गड करावा”
      and renamed the fort as Raigad and renovated and Expanded the fort, Hiroji Indulkar a skilled engineer and architect of his times, was given the task to build this fort. And announced this fort as the second capital of Swarajya. <br><br>
    </b></p>
    
