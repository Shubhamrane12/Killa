    <p><b>On 6th June 1674 Shivaji Maharaj was crowned as the king of Maratha Swaraj in a lavish ceremony,  In the Hindu calendar it was on the 13th day (trayodashi) of the first fortnight of the month of Jyeshtha in the year 1596.[97] Gaga Bhatt officiated, holding a gold vessel filled with the seven sacred waters of the rivers Yamuna, Indus, Ganges, Godavari, Narmada, Krishna and Kaveri over Maharaja's head, and chanted the Vedic coronation mantras.<br><br>
       After the ablution, Shivaji Maharaj bowed before Rajmata Jijabai and touched her feet. Nearly fifty thousand people gathered at Raigad for the ceremonies. Maharaj was entitled Shakakarta ("founder of an era") and Chhatrapati ("paramount sovereign"). He also took the title of Haindava Dharmodhhaarak (protector of the Hindu faith). Chhatrapati Shivaji Maharaj sat on the throne, ornamented with emblems and auspicious materials. Gaga Bhatt, a Hindu scholar conducted the ceremony. Maharaj was bestowed upon the title of ‘Kshatriya Kulavantas Chhatrapati Raje Shivaji’. It is also said that the Throne was made up of Gold, diamonds and various other ornaments.
      <br><br>
      After 12 days of the Coronation ceremony Rajmata Jijabai died on 18th June 1764. The Marathas summoned Bengali Tantrik goswami Nischal Puri, who declared that the original coronation had been held under inauspicious stars, and a second coronation was needed. This second coronation on 24 September 1674 had a dual use, mollifying those who still believed that Shivaji Maharaj was not qualified for the Vedic rites of his first coronation, by performing a less-contestable additional ceremony.<br><br>
      </b></p>
      
    <p><b>
      Campaign to south:
    </b></p>

    <p><b>
      In the mid of 1674, the Marathas undertook an aggressive campaign, raiding Khandesh (October), capturing Bijapuri Ponda (April 1675), Karwar (mid-year), and Kolhapur (July). In November the Maratha navy skirmished with the Siddis of Janjira, but failed to dislodge them. Having recovered from an illness, and taking advantage of a conflict between the Afghans and Bijapur, Chhatrapati Sambhaji Maharaj in the guidance of Chhatrapati Shivaji Maharaj raided Athani in April 1676.
       <br><br>
       In the run-up to his expedition Maharaj appealed to a sense of Deccani patriotism, that Southern India was a homeland that should be protected from outsiders. His appeal was somewhat successful, and in 1677 Chhatrapati Shivaji Maharaj visited Hyderabad for a month and entered into a treaty with the Qutubshah of the Golkonda sultanate, agreeing to reject his alliance with Bijapur and jointly oppose the Mughals. In 1677 Chhatrapati Shivaji Maharaj invaded Karnataka with 30,000 cavalry and 40,000 infantry, backed by Golkonda artillery and funding. Proceeding south, Shivaji seized the forts of Vellore and Gingee; the latter would later serve as a capital of the Marathas during the reign of his son Rajaram I.
       <br><br>
       Chhatrapati Shivaji Maharaj intended to reconcile with his half-brother Venkoji (Ekoji I), Shahaji Maharaj’s son by his second wife, Tukabai (née Mohite), who ruled Thanjavur (Tanjore) after Shahaji Maharaj. The initially promising negotiations were unsuccessful, so whilst returning to Raigad Chhatrapati Shivaji Maharaj defeated his half-brother's army on 26 November 1677 and seized most of his possessions in the Mysore plateau. Venkoji's wife Dipa Bai, whom Shivaji Maharaj deeply respected, took up new negotiations with Maharaj, and also convinced her husband to distance himself from Muslim advisors. 
       <br><br>
       After successfully completing the Campaign to south, after some years In late March 1680, Shivaji Maharaj fell ill with fever and dysentery, and died on 3rd April 1680 on the eve of Hanuman Jayanti. 
    </b></p>

