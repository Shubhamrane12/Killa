     <p><b>What to see on fort ?</b></p>
    <p><b>
      The Raigad Fort was built by Chhatrapati Shivaji Maharaj and the chief architect/engineer was Hiroji Indulkar. The main palace was constructed using wood, of which only the base pillars remain. The main fort ruins consist of the queen's quarters, six chambers, with each chamber having its own private restroom. In addition, ruins of three watch towers can be seen directly in front of the palace grounds out of which only two remain as the third one was destroyed during a bombardment. The Raigad Fort also has ruins of a market which was accessible to horseback riders. The fort also overlooks an artificial lake known as the Ganga Sagar Lake.<br><br>
      The only main pathway to the fort passes through the "Maha Darwaja" (Huge Door) which was previously closed at sunset. The Maha Darwaja has two huge bastions on both sides of the door which are approximately 65–70 feet in height. The top of the fort is 600 ft above this door.<br><br>
      The King's Darbar, inside the Raigad Fort has a replica of the original throne that faces the main doorway called the Nagarkhana Darwaja. This enclosure had been acoustically designed to aid hearing from the doorway to the throne. A secondary entrance, called the Mena Darwaja, was supposedly the private entrance for the royal ladies of the fort that lead to the queen's quarters. The convoy of the king and the king himself used the Palkhi Darwaja. To the right of Palkhi Darwaja, is a row of three dark and deep chambers. Historians believe that these were the granaries for the fort.<br><br>
      From the fort, one can view the execution point called Takmak Tok, a cliff from which sentenced prisoners were thrown to their death. This area has been fenced off.<br><br>
      The statue of Chhatrapati Shivaji Maharaj is erected in front of the ruins of the main market avenue that leads to the Jagdishwar Mandir and his own Samadhi and that of his loyal dog named Waghya. The Samadhi of Jijabai, Chatrapati Shivaji's mother, can be seen at base village of Pachad.<br><br>
      Additional famous attractions of the fort include the Khubladha Buruj, Nane Darwaja and the Hatti Talav (Elephant Lake).<br><br>
    </b></p>
    <p><b>Hirakani Buruj:</b></p>
    <p><b>The fort has a famous wall called "Hirakani Buruj" (Hirakani Bastion) constructed over a huge steep cliff. The legend goes "that a woman by the name of Hirakani from a nearby village had come to sell milk to the people living at the fort. She happened to be inside the fort when the gates got closed and locked at sunset. Hearing the cries of her infant son back at the village echo after nightfall, the anxious mother couldn't wait till dawn and courageously climbed down the steep cliff in pitch darkness all for the love of her little one. She later repeated this extraordinary feat in front of Chhatrapati Shivaji Maharaj and was bravely rewarded for it." In appreciation of her courage and bravery, Chhatrapati Shivaji Maharaj built the Hirakani Bastion over this cliff.</b></p>

    
